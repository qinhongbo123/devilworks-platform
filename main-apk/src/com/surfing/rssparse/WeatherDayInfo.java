package com.surfing.rssparse;

public class WeatherDayInfo
{
    private String mWeek;
    private int    mTemp_low;
    private int    mTemp_High;
    private String mIconUrl;
    private String mCondition;
    public String getmWeek()
    {
        return mWeek;
    }
    public void setmWeek(String mWeek)
    {
        this.mWeek = mWeek;
    }
    public int getmTemp_low()
    {
        return mTemp_low;
    }
    public void setmTemp_low(int mTemp_low)
    {
        this.mTemp_low = mTemp_low;
    }
    public int getmTemp_High()
    {
        return mTemp_High;
    }
    public void setmTemp_High(int mTemp_High)
    {
        this.mTemp_High = mTemp_High;
    }
    public String getmIconUrl()
    {
        return mIconUrl;
    }
    public void setmIconUrl(String mIconUrl)
    {
        this.mIconUrl = mIconUrl;
    }
    public String getmCondition()
    {
        return mCondition;
    }
    public void setmCondition(String mCondition)
    {
        this.mCondition = mCondition;
    }
    
}
