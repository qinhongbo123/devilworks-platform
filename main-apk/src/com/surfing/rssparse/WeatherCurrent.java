package com.surfing.rssparse;

public class WeatherCurrent
{
    private String mCondition;
    private int  mTemp_f;
    private int  mTemp_c;
    private String mHumidity;
    private String mIconUrl;
    private String mWind_condition;
    public String getmCondition()
    {
        return mCondition;
    }
    public void setmCondition(String mCondition)
    {
        this.mCondition = mCondition;
    }
    public int getmTemp_f()
    {
        return mTemp_f;
    }
    public void setmTemp_f(int mTemp_f)
    {
        this.mTemp_f = mTemp_f;
    }
    public int getmTemp_c()
    {
        return mTemp_c;
    }
    public void setmTemp_c(int mTemp_c)
    {
        this.mTemp_c = mTemp_c;
    }
    public String getmHumidity()
    {
        return mHumidity;
    }
    public void setmHumidity(String mHumidity)
    {
        this.mHumidity = mHumidity;
    }
    public String getmIconUrl()
    {
        return mIconUrl;
    }
    public void setmIconUrl(String mIconUrl)
    {
        this.mIconUrl = mIconUrl;
    }
    public String getmWind_condition()
    {
        return mWind_condition;
    }
    public void setmWind_condition(String mWind_condition)
    {
        this.mWind_condition = mWind_condition;
    }
    
}
